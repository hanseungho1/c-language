#include<stdio.h>

int main(void)
{
	int num1=9;
	int num2=2;
	
	printf("%d+%d=%d \n",num1,num2,num1+num2); 
	printf("%d-%d=%d \n",num1,num2,num1-num2);
	printf("%d*%d=%d \n",num1,num2,num1*num2);
	printf("%d/%d의 목=%d \n",num1,num2,num1/num2);
	printf("%d/%d 의 나머지=%d",num1,num2,num1%num2);

	return 0;
}
